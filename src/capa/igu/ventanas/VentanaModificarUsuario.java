    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
       
        CapaDatos.get().eliminar(usuario);
        
       usuario.setDni(  jTextFieldDNI.getText());
       usuario.setNombre( jTextFieldNombre.getText());
       usuario.setAp1( jTextFieldApellido1.getText());
       usuario.setAp2( jTextFieldApellido2.getText());
       usuario.setDir( jTextFieldDireccion.getText());
       usuario.setEmail( jTextFieldEmail.getText());
       usuario.setTel1( jTextFieldTel1.getText());
       usuario.setTel2( jTextFieldTel2.getText());
       usuario.setPob( jTextFieldPoblacion.getText());
       usuario.setProv( jTextFieldProvincia.getText());
        usuario.setTipo((String)comboTipo.getSelectedItem());
        if(jRadioButton2.isSelected())
            usuario.setSind("Empresa");// solo de sindicalista a empresario, no al revés
        
        CapaDatos.get().crearUsuario(usuario);
        
        
        
        this.setVisible(false);
        padre.setVisible(true);
    }//GEN-LAST:event_jButtonCrearActionPerformed

