    private void cargarUsuario() {

        jTextFieldDNI.setText(usuario.getDni());
        jTextFieldNombre.setText(usuario.getNombre());
        jTextFieldApellido1.setText(usuario.getAp1());
        jTextFieldApellido2.setText(usuario.getAp1());
        jTextFieldDireccion.setText(usuario.getDir());
        jTextFieldEmail.setText(usuario.getEmail());
        jTextFieldTel1.setText(usuario.getTel1());
        jTextFieldTel2.setText(usuario.getTel2());
        jTextFieldPoblacion.setText(usuario.getPob());
        jTextFieldProvincia.setText(usuario.getProv());
        comboTipo.setSelectedItem(usuario.getTipo());
        if(usuario.esSindicalista())
            jRadioButton1.setSelected(true);
        else
            jRadioButton2.setSelected(true);
    }
