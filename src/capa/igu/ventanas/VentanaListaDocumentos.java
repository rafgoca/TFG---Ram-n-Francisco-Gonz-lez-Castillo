    private void prepararTabla(){
        //debe filtrar
         lista = CapaIGU.get().buscarDocumentos(reunion);
         ArrayList<Documento> resultado= new ArrayList();
         String tipo = (String)tiposDoc.getSelectedItem();
         for(Documento doc: lista)
             if(doc.getTipo().equalsIgnoreCase(tipo))
                 resultado.add(doc);
        tablaDocumentos.setModel(new ModeloTablaDocumento(resultado));
    }

