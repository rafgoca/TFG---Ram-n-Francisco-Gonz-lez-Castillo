    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

     int[] seleccion =  tablaDocumentos.getSelectedRows();
     for(int i: seleccion){
          String ruta="";
         try {
             ruta  = (String) tablaDocumentos.getModel().getValueAt(i, 2);
             File fichero = new File(ruta);
             Desktop.getDesktop().open(fichero);
         } catch (IOException ex) {
            DialogoConfirmar.error("Imposible abrir el archivo "+ruta);
         }
     }
     
    }//GEN-LAST:event_jButton3ActionPerformed

    private void tiposDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tiposDocActionPerformed
       
       String tipo = (String)tiposDoc.getSelectedItem();
       if(tipo.equalsIgnoreCase("Todos")){
         tablaDocumentos.setModel(new ModeloTablaDocumento(lista));  
       }else{
           ArrayList<Documento> filtro = new ArrayList();
       for(Documento doc: lista){
           if(doc.getTipo().equalsIgnoreCase(tipo))
               filtro.add(doc);
       }
       tablaDocumentos.setModel(new ModeloTablaDocumento(filtro));
       }
    }//GEN-LAST:event_tiposDocActionPerformed

